"""utilities to run skcriteria tests.

"""

# =============================================================================
# IMPORTS
# =============================================================================

import os
import sys

import pytest


# =============================================================================
# CONSTANTS
# =============================================================================

PATH = os.path.abspath(os.path.dirname(__file__))


# =============================================================================
# IMPORTS
# =============================================================================

def run(argv=None):
    """Uses pytests.main to run the current path"""
    argv = argv or []
    return pytest.main([PATH] + argv)


# =============================================================================
# MAIN
# =============================================================================

if __name__ == "__main__":
    sys.exit(run(sys.argv[1:]))
